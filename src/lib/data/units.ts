export function mmToM(mm: number): number { return mm / 1000; }
export function mToMm(m: number): number { return m * 1000; }
export function kgHToTh(kgH: number): number { return kgH / 1000; }
export function thToKgH(th: number): number { return th * 1000; }
export function degToRad(deg: number): number { return (deg * Math.PI) / 180; }
export function radToDeg(rad: number): number { return (rad * 180) / Math.PI; }
